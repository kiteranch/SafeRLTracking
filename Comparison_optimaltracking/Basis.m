function [sig_p,L]=Basis(Z)
sig_p =[2*Z(1)        0             0             0;...
        Z(2)          Z(1)          0             0;...
        0             2*Z(2)        0             0;...
        2*Z(1)*Z(3)^2 0             2*Z(1)^2*Z(3) 0;...
        2*Z(1)*Z(4)^2 0             0             2*Z(1)^2*Z(4);...
        0             2*Z(2)*Z(3)^2 2*Z(2)^2*Z(3) 0;...
        0             2*Z(2)*Z(4)^2 0             2*Z(2)^2*Z(4)];   
L = size(sig_p,1);
end